#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int a = 3;
	int b = 2;
	
	ft_swap(&a, &b);
	printf("a = %d e b = %d",a, b);
}
