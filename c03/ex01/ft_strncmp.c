int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while ((*s1 != '\0' && *s2 != '\0') && ++i < n && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	if (n == 0)
	{
		return (0);
	}
	return (*s1 - *s2);
}
